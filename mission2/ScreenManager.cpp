#include "ScreenManager.h"

void ScreenManager::printCurrentStepScreen(int step) {
    switch (step) {
    case CarType_Q:
        printCarTypeScreen();
        break;
    case Engine_Q:
        printEngineScreen();
        break;
    case brakeSystem_Q:
        printBreakSystemScreen();
        break;
    case SteeringSystem_Q:
        printSteeringSystemScreen();
        break;
    case Run_Test:
        printRunTestScreen();
        break;
    }
    printf("===============================\n");
}

